using System;

class DoubleOrFloatNumber
{
    static void Main()
    {   // Print double or float number.
        double numberOne = 34.567839023;
        float numberTwo = 12.345f;
        double numberThree = 8923.1234857;
        float numberFour = 3456.091f;
        Console.WriteLine("Number one={0}", numberOne);
        Console.WriteLine("Number two={0}", numberTwo);
        Console.WriteLine("Number three={0}", numberThree);
        Console.WriteLine("Number four={0}", numberFour);
    }
}

